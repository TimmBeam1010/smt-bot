const fs = require('fs');
const path = require('path');

class ReportGenerator {
    constructor(config = {}) {
        this.outputDir = config.outputDir || './reports';
        this.templates = {};
        this.ensureDirectory();
    }

    /**
     * Создать директорию для отчетов
     */
    ensureDirectory() {
        if (!fs.existsSync(this.outputDir)) {
            fs.mkdirSync(this.outputDir, { recursive: true });
        }
    }

    /**
     * Сгенерировать HTML отчет
     */
    generateHTMLReport(data) {
        const template = `
<!DOCTYPE html>
<html>
<head>
    <meta charset="UTF-8">
    <title>SMT Bot Report - ${data.date || new Date().toISOString().split('T')[0]}</title>
    <style>
        body { font-family: monospace; background: #0a0a0a; color: #00ff88; padding: 20px; }
        h1 { color: #00ff88; border-bottom: 1px solid #00ff88; }
        .stats { display: grid; grid-template-columns: repeat(4, 1fr); gap: 10px; margin: 20px 0; }
        .stat { background: #1a1a1a; padding: 15px; border: 1px solid #00ff88; border-radius: 8px; }
        .stat-value { font-size: 24px; font-weight: bold; }
        .stat-label { font-size: 12px; color: #888; }
        .trades { margin-top: 20px; }
        .trade { padding: 8px; border-bottom: 1px solid #222; }
        .trade-profit { color: #00ff88; }
        .trade-loss { color: #ff2244; }
        .trade-side { font-weight: bold; }
        .trade-side-LONG { color: #00ff88; }
        .trade-side-SHORT { color: #ff2244; }
    </style>
</head>
<body>
    <h1>📊 SMT Bot Report</h1>
    <p>Date: ${data.date || new Date().toISOString().split('T')[0]}</p>
    <p>Time: ${data.time || new Date().toISOString().split('T')[1].slice(0, 8)}</p>
    
    <div class="stats">
        <div class="stat">
            <div class="stat-label">Balance</div>
            <div class="stat-value">$${data.balance?.toFixed(2) || '0'}</div>
        </div>
        <div class="stat">
            <div class="stat-label">Total Trades</div>
            <div class="stat-value">${data.totalTrades || 0}</div>
        </div>
        <div class="stat">
            <div class="stat-label">Win Rate</div>
            <div class="stat-value">${data.winRate?.toFixed(1) || '0'}%</div>
        </div>
        <div class="stat">
            <div class="stat-label">Profit</div>
            <div class="stat-value" style="color: ${data.profit >= 0 ? '#00ff88' : '#ff2244'}">
                $${data.profit?.toFixed(2) || '0'}
            </div>
        </div>
    </div>
    
    <h2>📈 Trading Summary</h2>
    <div class="trades">
        ${data.trades?.map(trade => `
            <div class="trade">
                <span class="trade-side trade-side-${trade.side}">${trade.symbol} ${trade.side}</span>
                @ ${trade.entryPrice} → ${trade.exitPrice || 'open'}
                <span class="${trade.profit >= 0 ? 'trade-profit' : 'trade-loss'}">
                    ${trade.profit >= 0 ? '+' : ''}${trade.profit?.toFixed(2) || '0'}%
                </span>
            </div>
        `).join('') || '<p>No trades yet</p>'}
    </div>
    
    <hr>
    <p style="color: #666; font-size: 12px;">Generated by SMT Bot v2.0</p>
</body>
</html>
        `;

        return template;
    }

    /**
     * Сохранить отчет
     */
    saveReport(data, filename = null) {
        const date = new Date().toISOString().split('T')[0];
        const name = filename || `report-${date}.html`;
        const filepath = path.join(this.outputDir, name);
        
        const html = this.generateHTMLReport(data);
        fs.writeFileSync(filepath, html);
        console.log(`📄 Отчет сохранен: ${filepath}`);
        return filepath;
    }

    /**
     * Сгенерировать и сохранить отчет
     */
    generate(data) {
        const filepath = this.saveReport(data);
        return filepath;
    }
}

module.exports = { ReportGenerator };
